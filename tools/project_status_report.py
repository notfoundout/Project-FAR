#!/usr/bin/env python3
from __future__ import annotations

import json
from pathlib import Path

from report_link_utils import markdown_link

ROOT = Path(__file__).resolve().parents[1]
OUT = ROOT / "docs/reports/project-status-generated.md"


def load(path: str) -> dict:
    return json.loads((ROOT / path).read_text(encoding="utf-8"))


def nav() -> list[str]:
    return [
        f"- README Command Center: {markdown_link(ROOT / 'README.md', OUT)}",
        f"- Project Status: {markdown_link(OUT, OUT)}",
        f"- Research Gaps: {markdown_link(ROOT / 'docs/reports/research-gap-report.md', OUT)}",
        f"- Next Actions: {markdown_link(ROOT / 'docs/planning/next-actions.md', OUT)}",
    ]


def main() -> int:
    candidate = load("theory/evaluation/w3-5-candidate-test-result-v1.0.json")
    gate = load("theory/evaluation/w3-5-specificity-and-discovery-gate.json")
    scope = load("theory/evaluation/reasoning-and-contrast-scope-v1.0.json")
    dimensions = load("theory/evaluation/w3-5-factorization-result-v1.0.json")["dimensions"]
    discrimination = load("theory/evaluation/w3-5-reasoning-discrimination-result-v1.0.json")["registered_results"]
    specificity = load("theory/evaluation/w3-5-fara-specificity-result-v1.0.json")["result"]
    structural = candidate["axis_counts"]["structural_commitment_necessity"]
    primitive = candidate["axis_counts"]["primitive_necessity"]
    explicit = candidate["axis_counts"]["explicit_representation_necessity"]
    reasoning = candidate["axis_counts"]["reasoning_specificity"]
    architecture = candidate["axis_counts"]["architecture_invariance"]

    lines = [
        "# Project Status (Generated)",
        "",
        "## Navigation",
        "",
        *nav(),
        "",
        "Generated by `python tools/project_status_report.py`.",
        "",
        "## Current Research Mode",
        "",
        f"- W3.5 status: `{gate['status']}`.",
        "- Corpus, factorization, registered discrimination, qualified FARA-specificity, registered candidate execution, machinery/cost closure, claim-impact audit, and preserved-failure closure are complete.",
        "- Candidate execution preserves 648 of 648 required atomic trials.",
        "- W5 theorem assembly is authorized to begin.",
        "- No theorem is proved by W3.5 closure.",
        "",
        "## Registered ADJ Results",
        "",
        f"- Corpus: {len(scope['positive_instances'])} positive, {len(scope['contrast_instances'])} contrast, {len(scope['disputed_instances'])} disputed.",
        f"- Factorization: `{dimensions['overall_interpretation']}`; expressiveness `{dimensions['expressiveness']}`; translation `{dimensions['translation']}`; cost `{dimensions['cost_relation']}`.",
        f"- Discrimination: {discrimination['positive']['reasoning_like']}/8 positives reasoning-like; {discrimination['contrast']['nonreasoning_like']}/8 contrasts nonreasoning-like; {discrimination['disputed']['borderline']}/2 disputed borderline.",
        f"- FARA specificity: `{specificity['classification']}`; unique capacity `{specificity['unique_discriminative_capacity_of_fara']}`.",
        f"- Candidate aggregate: `{candidate['aggregate_result']}`.",
        f"- Primitive necessity: supported {primitive['supported_at_registered_scope']}, refuted {primitive['refuted_at_registered_scope']}, partial {primitive['partial']}, unresolved {primitive['unresolved']}.",
        f"- Explicit-representation necessity: supported {explicit['supported_at_registered_scope']}, refuted {explicit['refuted_at_registered_scope']}, partial {explicit['partial']}, unresolved {explicit['unresolved']}.",
        f"- Structural-commitment necessity: supported {structural['supported_at_registered_scope']}, refuted {structural['refuted_at_registered_scope']}, partial {structural['partial']}, unresolved {structural['unresolved']}.",
        f"- Reasoning specificity: supported {reasoning['supported_at_registered_scope']}, refuted {reasoning['refuted_at_registered_scope']}, partial {reasoning['partial']}, unresolved {reasoning['unresolved']}.",
        f"- Architecture invariance: supported {architecture['supported_at_registered_scope']}, refuted {architecture['refuted_at_registered_scope']}, partial {architecture['partial']}, unresolved {architecture['unresolved']}.",
        "- Candidate evidence status: complete project-authored internal execution; not independent replication.",
        "- Cross-package cost result: tradeoff preserved; no superiority claim authorized.",
        "",
        "## Comparative Representation Status",
        "",
        "CRE-001: deterministic comparison complete at its registered retrospective scope under compiler-authored declared interpretations.",
        "",
        "## USD Status",
        "",
        "- Universal structure: unresolved.",
        "- No universal nonexistence, universal necessity, minimality, or uniqueness conclusion is authorized.",
        "",
        "## Navigation",
        "",
        *nav(),
    ]
    OUT.write_text("\n".join(lines) + "\n", encoding="utf-8")
    print(OUT.relative_to(ROOT))
    return 0


if __name__ == "__main__":
    raise SystemExit(main())

#!/usr/bin/env python3
"""Generate advisory next actions from generated status and gap reports."""
from __future__ import annotations
from pathlib import Path
import re, yaml
ROOT=Path(__file__).resolve().parents[1]
OUT=ROOT/'docs/planning/next-actions.md'
GAP=ROOT/'docs/reports/research-gap-report.md'
STATUS=ROOT/'docs/reports/project-status-generated.md'
SEV={'critical':0,'high':1,'medium':2,'low':3}

def parse_gaps():
    rows=[]
    if not GAP.exists(): return rows
    for line in GAP.read_text(encoding='utf-8').splitlines():
        if not line.startswith('| GAP-'): continue
        parts=[p.strip() for p in line.strip('|').split('|')]
        if len(parts)>=5: rows.append(dict(id=parts[0], type=parts[1], loc=parts[2].strip('`'), sev=parts[3], action=parts[4]))
    return rows

def task_from_gap(g):
    slug=re.sub(r'[^a-z0-9]+','-',g['type'].lower()).strip('-')[:40]
    return {
      'id': 'TASK-'+g['id'].split('-')[-1], 'priority': g['sev'],
      'title': f"Review {g['type']} at {g['loc']}", 'why': g['action'],
      'files': g['loc'], 'outcome': 'Human-reviewed resolution plan or advisory documentation update; no automatic theory change.',
      'risk': 'high' if g['sev'] in ('critical','high') else 'medium' if g['sev']=='medium' else 'low',
      'branch': f"codex/review-{slug}-{g['id'].lower()}",
      'pr': f"Review {g['type']} ({g['id']})"
    }

def main():
    gaps=parse_gaps(); gaps.sort(key=lambda g:(SEV.get(g['sev'],9), g['id']))
    tasks=[task_from_gap(g) for g in gaps[:20]]
    if not tasks:
        tasks=[{'id':'TASK-001','priority':'low','title':'Review generated status report','why':'Keep advisory planning current.','files':'docs/reports/project-status-generated.md','outcome':'Human confirms planner output remains aligned with evidence.','risk':'low','branch':'codex/review-generated-status','pr':'Review generated Project FAR status'}]
    lines=['# Next Actions','', 'Generated by `python tools/generate_next_tasks.py`. These recommendations are advisory only and do not authorize theory changes.','', '## Ranked Next Actions','']
    for t in tasks:
        lines += [f"### {t['id']}: {t['title']}",'',f"- Priority: {t['priority']}",f"- Why it matters: {t['why']}",f"- Affected files: `{t['files']}`",f"- Expected outcome: {t['outcome']}",f"- Risk level: {t['risk']}",f"- Suggested branch name: `{t['branch']}`",f"- Suggested PR title: `{t['pr']}`",'']
    lines += ['## Codex Prompts','']
    for t in tasks[:5]:
        lines += [f"### Prompt for {t['id']}",'','```markdown',f"Create branch `{t['branch']}`.",'',f"Scope: {t['title']}. This is advisory planning or documentation only.",f"Allowed files: `{t['files']}`, generated reports, and planning documentation directly needed for this review.",'Forbidden files: primitives, definitions, axioms, theorem statements, proof objects, parser behavior, reasoning-engine behavior, metadata schemas, and evaluation conclusions.','Validation commands:','- `python tools/self_advancement_plan.py`','- `python tools/repo_health_check.py --fast` if available',f"PR title: {t['pr']}",'Stop condition: stop before any theory change; request human review if the work would alter accepted theory or conclusions.','```','']
    OUT.parent.mkdir(parents=True, exist_ok=True); OUT.write_text('\n'.join(lines),encoding='utf-8')
    print(f"{OUT.relative_to(ROOT)} tasks={len(tasks)}")
    return 0
if __name__=='__main__': raise SystemExit(main())
